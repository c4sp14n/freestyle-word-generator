import json
import os

def convert_file(input_file, output_file, descriptions_map):
    if not os.path.exists(input_file):
        print(f"File not found: {input_file}")
        return
        
    with open(input_file, 'r', encoding='utf-8') as f:
        words = [line.strip() for line in f if line.strip()]
    
    json_output = []
    for word in words:
        # Try exact match, then lowercase match
        desc = descriptions_map.get(word) or descriptions_map.get(word.lower()) or f"Term: {word}"
        json_output.append({
            "word": word,
            "description": desc
        })
    
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(json_output, f, ensure_ascii=False, indent=2)
    print(f"Converted {input_file} to {output_file} ({len(json_output)} words)")

